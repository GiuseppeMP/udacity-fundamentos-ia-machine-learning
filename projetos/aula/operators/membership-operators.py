'''in	True if value/variable is found in the sequence	5 in x
not in	True if value/variable is not found in the sequence	5 not in x'''

x = 'Hello world'
y = {1:'a',2:'b'}

# Output: True
print('H' in x)

# Output: True
print('hello' not in x)

# Output: True
print(1 in y)

# Output: False
print('a' in y)
